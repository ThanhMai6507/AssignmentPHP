<?php 
    function sumOfArray($array) 
    {
        $sum = array_sum($array);
        echo "Sum of array is: $sum";
    }

    function maxOfArray($array) 
    {
        $max = max($array);
        echo "\nMax element of array is: $max";
    }

    function minOfArray($array)
    {
        $min = min($array);
        echo "\nMin element of array is: $min";
    }
?>
